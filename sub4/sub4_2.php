                    <h3>Economics of Ecosystems</h3>
                    <p>Our ecosystems provide water and food for billions of people while also protecting them from natural hazards and the worst impacts of climate change. Yet despite a growing number of assessments showing that ecosystem services are worth billions of dollars to national economies, most governments and businesses are still not including ecosystem valuations in policy decisions. 
A better understanding of our economic reliance on ecosystems can 
prompt behaviour change. Failure to recognize and account for the value of these goods and services in government and private sector decision-making has led to 
the unsustainable exploitation of ecosystems, triggering severe 
long-term social and economic costs which are increasingly reflected in insurance premiums, food prices, health incidents and even civil unrest.</p>
                  <p>What does UNEP do to include the value of ecosystems in decision-making?</p>
                  <ul>
                    <li>promotes awareness of our dependence on ecosystems 
and their economic impact;</li>
                    <li>develops and promotes knowledge products and tools on how to value and account for ecosystem goods and services; and,</li>
                    <li>works with countries to build expertise on how to value and include natural capital in national statistics and as key criteria in policy-making.</li>
                  </ul>
 
