                          <h5>The Programme</h5>
                          <p>The United Nations Collaborative Programme on Reducing 
Emissions from Deforestation and Forest Degradation in Developing 
Countries was launched in 2008 and builds on the convening 
capacity and technical expertise of the Food and Agriculture 
Organization of the United Nations (FAO), the United Nations Development 
Programme (UNDP) and the United Nations Environment Programme (UNEP).
​The overall development goal of the Programme is to reduce forest 
emissions and enhance carbon stocks in forests while contributing to 
national sustainable development.
The UN-REDD Programme supports nationally led REDD+ processes and 
promotes the informed and meaningful involvement of all stakeholders, 
including indigenous peoples and other forest-dependent communities, in 
the implementation of REDD+ activities agreed under the UNFCCC.
The Programme has expanded steadily since its establishment and over 60 
countries spanning Africa, Asia-Pacific and Latin America are now partners.</p>
						<p>We supports partner countries through:
-Direct support to the design and implementation of 
National REDD+ Programmes;
