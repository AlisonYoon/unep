                        <h4>Why is REDD+ important?</h4>
                        <p>Deforestation and forest degradation have long been
recognized as significant sources of carbon emissions,
as trees store carbon and when they are destroyed this
carbon is released into the atmosphere contributing to
greenhouse gases that cause climate change.
As up to 11 per cent of carbon emissions are caused by
deforestation and forest degradation, it is important that
the reduction of these emissions is part of the global
plan to fight climate change. REDD+ is the identified
mechanism to do so.
In addition to their carbon storage role, forests are
valuable in many other ways. This includes water
regulation, soil protection, non-timber forest products
including food and fibre, climate regulation and
biodiversity. In fact, it is estimated that 1.6 billion people
depend on forests. As such, by conserving forests,
REDD+ offers a broad range of social, environmental and
economic benefits to developing countries and forest
communities.</p>
