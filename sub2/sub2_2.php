                        <h4>How does REDD+ benefit developing
countries?</h4>

<p>Developing countries that meet UNFCCC REDD+
requirements (see box “Elements of UNFCCC Warsaw
Framework for REDD+”) will receive results-based
payments for verified emissions reductions. As such,
REDD+ creates an incentive for these countries to reduce
emissions from forests, and invest in low-carbon paths to
sustainable development.
By implementing REDD+ actions, countries will
contribute to conserving their national biodiversity and
to the global fight against climate change. In addition to
the environmental benefits, REDD+ also offers social and
economic benefits. Most recently, it is being integrated
into green economy strategies.
REDD+ is also a tool to realize achievement of the
transformative 2030 Agenda for Sustainable
Development adopted by world leaders in September
2015. The new agenda calls on countries to begin efforts
to achieve 17 Sustainable Development Goals over the
next 15 years. While REDD+ is directly linked to Climate
Action (goal 13), it can also play an important role
towards Responsible Consumption and Production (goal
12), Gender Equality (goal 5), Life on Land (goal 15), and
Decent Work and Economic Growth (goal 8) among
others.</p>
