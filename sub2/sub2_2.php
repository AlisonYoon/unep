                        <h4>About REDD+</h4>
                          <p>Deforestation and forest degradation account for approximately 
17 per cent of carbon emissions, more than the entire global transportation 
sector and second only to the energy sector. It is now clear that in 
order to constrain the impacts of climate change within limits that society will 
reasonably be able to tolerate, global average temperatures must be 
stabilized within two degrees Celsius. This will be practically impossible to 
achieve without reducing emissions from the forest sector, in addition to 
other mitigation actions.
Reducing emissions from deforestation and forest degradation (REDD+) 
is a mechanism developed by Parties to the United Nations Framework 
Convention on Climate Change (UNFCCC). It creates a financial 
value for the carbon stored in forests by offering incentives for developing 
countries to reduce emissions from forested lands and invest in 
low-carbon paths to sustainable development. Developing countries 
would receive results-based payments for results-based actions. 
REDD+ goes beyond simply deforestation and forest degradation, and 
includes the role of conservation, sustainable management of forests 
and enhancement of forest carbon stocks.</p>
