                 <section class="textMiddle">
                  <p>Sustainable consumption and production is defined as “the use of services and 
related products, which respond to basic needs and bring a better quality of life 
while minimising the use of natural resources and toxic materials as well as the 
emissions of waste and pollutants over the life cycle of the service or product so 
as not to jeopardise the needs of future generations.” Norwegian Ministry of 
Environment, Oslo Symposium, 1994.</p>
<p>SCP aims at “doing more and better with less,” increasing net welfare gains 
from economic activities by reducing resource use, degradation and pollution 
along the whole lifecycle, while increasing quality of life. This change towards 
SCP involves different stakeholders, including business, consumers, policy makers, 
researchers, scientists, retailers, media, and development cooperation agencies, 
among others. It requires a systemic approach and cooperation among actors 
operating in the supply chain, from producer to final consumer. It involves engaging 
consumers through awareness-raising and education on sustainable consumption 
and lifestyles, providing consumers with adequate information through standards 
and labels and engaging in sustainable public procurement, among others.</p>
<p>Sustainable consumption has been recognized as an integral element of sustainable 
development and an issue of paramount importance. The UN Conference on the 
Environment and Development held in Rio de Janeiro in 1992 recognized sustainable 
consumption and production as an overarching theme to link environmental and 
developmental challenges. Agenda 21 states that the major cause of the continued 
deterioration of the global environment is the unsustainable patterns of consumption 
and production. Ten years later in 2002, world leaders signed the Johannesburg 
Plan of Implementation at the World Summit on Sustainable Development (WSSD). 
Chapter 3 of the Plan was devoted to “Changing Unsustainable Patterns of 
Consumption and Production” and declared that “fundamental changes in the way 
societies produce and consume are indispensable for achieving global sustainable 
development. All countries should promote sustainable consumption and production 
patterns.” In 2012, at the UN Conference on Sustainable Development (Rio +20), 
countries recognized that fundamental changes in the way societies consume and 
produce are indispensable for achieving global sustainable development, 
and adopted the 10-year framework of programmes on sustainable consumption 
and production patterns.</p>
